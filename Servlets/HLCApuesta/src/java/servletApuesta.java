            String eleccion = request.getParameter("eleccion");
            
            String cantidadApostada = request.getParameter("cantidad");
            double cantidad =(double) Double.parseDouble(cantidadApostada);
            
            String cuota = request.getParameter("cuota");
            double cuotaNum = (double) Double.parseDouble(cuota);
            
            double premio = 0;
            
            if (resultado.equals(eleccion)){
                premio = cantidad*cuotaNum;
            }

            /* TODO output your page here. You may use following sample code. */
